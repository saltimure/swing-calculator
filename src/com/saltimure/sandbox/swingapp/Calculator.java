package com.saltimure.sandbox.swingapp;

public class Calculator {
    
    public Calculator() {
    }
    
    public int add(int n1, int n2) {
        return n1 + n2;
    }
    
    public int substract(int n1, int n2) {
        return n1 - n2;
    }
    
    public int multiply(int n1, int n2) {
        return n1 * n2;
    }
    
    public int divide(int n1, int n2) {
        return n1 / n2;
    }
}
